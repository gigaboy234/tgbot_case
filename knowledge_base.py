def normalize_text(text: str) -> str:
    return ' '.join(text.lower().replace('ё', 'е').split())


def classify_question(text: str) -> str:
    normalized = normalize_text(text)
    scores = {'documents': 0, 'admin': 0, 'hr': 0}
    category_keywords = {
        'documents': ['отпуск', 'заявление', 'документ', 'согласование', 'командировка', 'справка', 'табель', 'больничный', 'записка'],
        'admin': ['офис', 'адрес', 'контакт', 'график', 'подразделение', 'оргструктура', 'пропуск', 'ноутбук', 'техника', 'парковка'],
        'hr': ['hr', 'кадры', 'политика', 'испытательный', 'льготы', 'дмс', 'зарплата', 'премия', 'увольнение', 'декрет', 'конфликт', 'перевод'],
    }
    for category, keywords in category_keywords.items():
        for kw in keywords:
            if kw in normalized:
                scores[category] += 1
    best_category = max(scores, key=scores.get)
    if scores[best_category] == 0:
        return 'unknown'
    return best_category


def find_best_entry(text: str) -> Optional[KBEntry]:
    normalized = normalize_text(text)
    category = classify_question(text)
    if category == 'unknown':
        return None
    candidates = [entry for entry in KB_ENTRIES if entry.category == category]
    best_entry = None
    best_score = 0
    for entry in candidates:
        score = 0
        for pattern in entry.patterns:
            if pattern in normalized:
                score += max(1, len(pattern.split()))
        if score > best_score:
            best_score = score
            best_entry = entry
    return best_entry


def should_escalate(text: str, entry: Optional[KBEntry]) -> bool:
    normalized = normalize_text(text)
    hard_escalation_keywords = [
        'индивидуальный случай', 'нестандартная ситуация', 'жалоба', 'конфликт', 'увольнение',
        'нарушение', 'дискриминация', 'домогательство', 'персональные данные', 'зарплата не пришла',
        'не выплатили', 'ошибка в зарплате',
    ]
    if entry and entry.escalation_required:
        return True
    return any(kw in normalized for kw in hard_escalation_keywords)
